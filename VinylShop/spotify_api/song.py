class Song:

    def __init__(self, name, song_id, album, artist, artistID, artist_img=None, album_img=None):
        self.name = name
        self.song_id = song_id
        self.album = album
        self.artist = artist
        self.artistID = artistID
        self.artist_img = artist_img
        self.album_img = album_img

